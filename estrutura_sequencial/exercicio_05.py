"""
12 - Tendo como dados de entrada a altura de uma pessoa, 
construa um algoritmo que calcule seu peso ideal, 
usando a seguinte fórmula: (72.7*altura) - 58
"""
# Entrada de dados:
altura_de_uma_pessoa = float(input("Digite sua altura: "))


# Processamento de dados:
peso_ideal = 72.7 * altura_de_uma_pessoa - 58.0


# Saida de dados:
print(f" O peso seu peso ideal é = '{peso_ideal} kg'")






