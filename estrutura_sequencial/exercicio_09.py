"""
6 - Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.
"""

# Entrada de dados:
import math

valor_do_raio = float(input("Digite o valor do raio: "))

# Processamento de dados: 
area_da_circunferencia = math.pi * valor_do_raio ** 2


# Saida de dados:
print(f"A circunferência de raio {valor_do_raio} tem o valor {area_da_circunferencia} em area")



