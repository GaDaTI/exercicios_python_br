"""
3 - Faça um Programa que peça dois números e 
imprima a soma.
"""

# Entrada de dados
valor_1 = int(input("Digite um numero: "))
valor_2 = int(input("Digite outro numero: "))
print()

# Processamento de dados: Soma de dois numeros
soma = valor_1 + valor_2

# Saida de dados
print(f"O valor da soma de {valor_1} e {valor_2} é igual a {soma}.")
